#!/usr/bin/env python3
"""Cook a module's compiled-in translation matrix from i18n/modules/<module>/<lang>.json.

Each <lang>.json is a flat { "MS_ID": "text", ... } map. en_US.json is canonical: it defines
the id set and order. Emits <out>/<module>_strings_gen.{h,c} -- an enum of ids plus an
[id][lang] string matrix that the module compiles into its own binary (no device-side files).
At runtime ms_set_lang(code) picks the column from the active locale code (the host
gui->lang_code()), and ms(id) returns the active translation, falling back to English (column
0) for a missing language or cell. The build (`make i18n`, and each module's link) runs this,
so module translations are cooked by the same mechanism as the core packs.
"""
import argparse, json, os, re, sys

# RTL module strings are PRE-SHAPED at cook time (like the core packs): reshape() picks the
# contextual presentation form, get_display() reorders to visual order, so the device draws
# the bytes left-to-right with no runtime shaping. The MP3 layout is NOT mirrored (the user
# does not want a full RTL interface for it) -- only the text is the language's.
RTL_LANGS = {"ar", "fa_IR"}


def _shaper():
    try:
        import arabic_reshaper
        from bidi.algorithm import get_display
    except ImportError:
        sys.exit("RTL module strings need arabic_reshaper + python-bidi "
                 "(pip install arabic-reshaper python-bidi)")
    return lambda s: get_display(arabic_reshaper.reshape(s))


_TOKEN_RE = re.compile(r"\{[A-Za-z0-9_]+\}")


def _shape_tokens(shape, s):
    """Reshape only the non-token segments; keep {BTN_*} glyph tokens as left-to-right units
    in place. So a footer like "{BTN_A} موافق" keeps its L-to-R structure (the MP3 UI is not
    mirrored) while the Arabic verb between/around tokens is correctly shaped to visual order."""
    out, last = [], 0
    for m in _TOKEN_RE.finditer(s):
        out.append(shape(s[last:m.start()]))
        out.append(m.group())
        last = m.end()
    out.append(shape(s[last:]))
    return "".join(out)


def c_str(s):
    out = []
    for ch in s:
        if ch == '\\':   out.append('\\\\')
        elif ch == '"':  out.append('\\"')
        elif ch == '\n': out.append('\\n')
        elif ch == '\t': out.append('\\t')
        elif ch == '\r': out.append('\\r')
        else:            out.append(ch)   # UTF-8 passes through (the source file is UTF-8)
    return '"' + ''.join(out) + '"'


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("module", help="module name, e.g. mp3 (-> i18n/modules/mp3/)")
    ap.add_argument("--src", default=None, help="override the i18n/modules/<module> source dir")
    ap.add_argument("--out", required=True, help="output dir for <module>_strings_gen.{h,c}")
    ap.add_argument("--chars-append", default=None,
                    help="append RTL (ar/fa) reshaped codepoints to this .chars file (the Arabic font subset)")
    a = ap.parse_args()

    repo = os.path.dirname(os.path.dirname(os.path.dirname(os.path.abspath(__file__))))
    src = a.src or os.path.join(repo, "i18n", "modules", a.module)
    en_path = os.path.join(src, "en_US.json")
    if not os.path.exists(en_path):
        sys.exit(f"cook_modstrings: missing {en_path} (en_US.json is the canonical id set)")

    en = json.load(open(en_path, encoding="utf-8"))
    ids = list(en.keys())                       # canonical id order

    langs = ["en_US"]                           # English first = the fallback column
    for fn in sorted(os.listdir(src)):
        if fn.endswith(".json") and fn != "en_US.json":
            langs.append(fn[:-5])
    data = {lc: json.load(open(os.path.join(src, lc + ".json"), encoding="utf-8")) for lc in langs}

    # Pre-shape RTL languages to visual order (the device does no runtime shaping), and feed
    # their reshaped glyphs to the Arabic font subset so they actually render on device.
    if any(lc in RTL_LANGS for lc in langs):
        shape = _shaper()
        rtl_cps = set()
        for lc in langs:
            if lc in RTL_LANGS:
                data[lc] = {k: _shape_tokens(shape, v) for k, v in data[lc].items()}
                for v in data[lc].values():
                    rtl_cps |= {ch for ch in v if ord(ch) >= 0x80}
        if a.chars_append and rtl_cps:
            with open(a.chars_append, "a", encoding="utf-8") as cf:
                cf.write("".join(sorted(rtl_cps)))

    os.makedirs(a.out, exist_ok=True)
    base = f"{a.module}_strings_gen"
    guard = base.upper() + "_H"

    with open(os.path.join(a.out, base + ".h"), "w", encoding="utf-8") as f:
        f.write(f"/* Generated by scripts/build/cook_modstrings.py from i18n/modules/{a.module}/*.json. DO NOT EDIT. */\n")
        f.write(f"#ifndef {guard}\n#define {guard}\n\nenum {{\n")
        for i, k in enumerate(ids):
            f.write(f"    {k} = {i},\n")
        f.write("    MS_COUNT\n};\n\n")
        f.write("/* Pick the active column from a locale code (call once per run with gui->lang_code()). */\n")
        f.write("void ms_set_lang(const char *code);\n")
        f.write("/* The active translation for `id` (English fallback). Never NULL. */\n")
        f.write("const char *ms(int id);\n\n")
        # The feature menu shows MODULE_MENU_LABEL_XLAT, which the core reads from the module
        # header at DISCOVERY (no module load). Pack MS_TITLE per language as
        # "code\0title\0...\0" so the core can resolve the active language live. #undef first so
        # this wins regardless of include order vs module.h's "" default.
        if "MS_TITLE" in en:
            lit = lambda s: c_str(s)[1:-1]
            xlat = "".join(lit(lc) + "\\0" + lit(data[lc].get("MS_TITLE", "")) + "\\0" for lc in langs) + "\\0"
            f.write("/* MS_TITLE per language, read from the module header at discovery for the menu. */\n")
            f.write("#undef MODULE_MENU_LABEL_XLAT\n")
            f.write(f'#define MODULE_MENU_LABEL_XLAT "{xlat}"\n\n')
        f.write(f"#endif /* {guard} */\n")

    with open(os.path.join(a.out, base + ".c"), "w", encoding="utf-8") as f:
        f.write(f"/* Generated by scripts/build/cook_modstrings.py from i18n/modules/{a.module}/*.json. DO NOT EDIT. */\n")
        f.write(f'#include "{base}.h"\n#include <string.h>\n\n')
        f.write("static const char *const LANGS[] = { " + ", ".join(c_str(l) for l in langs) + " };\n")
        f.write("#define NL ((int)(sizeof(LANGS) / sizeof(LANGS[0])))\n\n")
        f.write("/* [id][lang]; a 0 (NULL) cell falls back to the English column (0). */\n")
        f.write("static const char *const T[MS_COUNT][NL] = {\n")
        for k in ids:
            cells = [c_str(data[lc][k]) if k in data[lc] else "0" for lc in langs]
            f.write(f"    {{ {', '.join(cells)} }},   /* {k} */\n")
        f.write("};\n\n")
        f.write("static int g_col;\n\n")
        f.write("void ms_set_lang(const char *code) {\n")
        f.write("    g_col = 0;\n")
        f.write("    if (code) for (int i = 0; i < NL; i++) if (strcmp(code, LANGS[i]) == 0) { g_col = i; break; }\n")
        f.write("}\n\n")
        f.write("const char *ms(int id) {\n")
        f.write('    if (id < 0 || id >= MS_COUNT) return "";\n')
        f.write("    const char *s = T[id][g_col];\n")
        f.write("    return s ? s : T[id][0];\n")
        f.write("}\n")

    print(f"cook_modstrings: {a.module}: {len(ids)} ids x {len(langs)} langs -> {os.path.join(a.out, base)}.[hc]")


if __name__ == "__main__":
    main()
