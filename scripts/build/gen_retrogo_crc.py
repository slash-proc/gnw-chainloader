#!/usr/bin/env python3
"""Generate src/common/retrogo_crc.h from the built Retro-Go internal-flash image.

Boot-time integrity gate for the Retro-Go launcher payload (the analogue of the OFW
CRC gate; see scripts/build/gen_ofw_crc.py and src/chainloader/system/ofw_verify.c).
Retro-Go lives in Bank 1 at RETROGO_BASE; a partial/interrupted flash leaves it
unbootable, and jumping into it would crash before the chainloader menu is reachable
(STABILITY IS LAW). The chainloader re-computes this CRC on the device before launching
Retro-Go and falls back to its own menu on a mismatch.

This bakes the CRC of retro-go-sd/build/gw_retro_go_intflash.bin -- the exact bytes
flashed at RETROGO_BASE -- into a committed header. Regenerate + recommit after
rebuilding Retro-Go:  make build-rg-sd && make retrogo-crc.

CRC convention is identical to the OFW gate: STM32H7 hardware CRC in its reset-default
config (poly 0x04C11DB7, init 0xFFFFFFFF, no reflection, no final XOR -- CRC-32/MPEG-2),
fed one little-endian 32-bit word at a time, exactly as ofw_crc32() does on the device.
The image is truncated to a whole number of 32-bit words (the device CRCs len>>2 words);
the dropped <4 trailing bytes are inconsequential for detecting a partial flash.
"""
import sys
from pathlib import Path

REPO = Path(__file__).resolve().parents[2]
RG_BIN = REPO / "retro-go-sd" / "build" / "gw_retro_go_intflash.bin"
OUT = REPO / "src" / "common" / "retrogo_crc.h"


def crc32_mpeg2_words(data: bytes) -> int:
    """Mirror of gen_ofw_crc.crc32_mpeg2_words: HW CRC fed LE 32-bit words."""
    if len(data) % 4:
        raise ValueError(f"CRC window length {len(data)} is not a multiple of 4")
    crc = 0xFFFFFFFF
    for i in range(0, len(data), 4):
        crc ^= int.from_bytes(data[i : i + 4], "little")
        for _ in range(32):
            crc = ((crc << 1) ^ 0x04C11DB7) & 0xFFFFFFFF if crc & 0x80000000 else (crc << 1) & 0xFFFFFFFF
    return crc


def main() -> int:
    if not RG_BIN.is_file():
        sys.exit(f"Retro-Go image not found: {RG_BIN}\nBuild it first: make build-rg-sd")
    data = RG_BIN.read_bytes()
    length = len(data) & ~3                      # whole 32-bit words (device CRCs len>>2)
    crc = crc32_mpeg2_words(data[:length])

    OUT.write_text(
        "#ifndef COMMON_RETROGO_CRC_H\n"
        "#define COMMON_RETROGO_CRC_H\n"
        "\n"
        "/*\n"
        " * GENERATED by scripts/build/gen_retrogo_crc.py -- DO NOT EDIT BY HAND.\n"
        " * Regenerate after rebuilding Retro-Go:  make build-rg-sd && make retrogo-crc\n"
        " *\n"
        " * Baked CRC-32/MPEG-2 of the Retro-Go internal-flash image (the bytes flashed at\n"
        " * RETROGO_BASE). The chainloader re-checks it on the device before launching\n"
        " * Retro-Go and falls back to its own menu on a mismatch (partial/corrupt flash).\n"
        " * See src/chainloader/system/ofw_verify.c (retrogo_crc_ok) and DESIGN.md.\n"
        " *\n"
        " * RETROGO_CRC_LEN == 0 means \"not baked\": the device gate fails OPEN (boots\n"
        " * Retro-Go) so a tree without a regenerated header is never bricked.\n"
        " */\n"
        "\n"
        "#include <stdint.h>\n"
        "\n"
        f"#define RETROGO_CRC_LEN  0x{length:08X}u   /* bytes of the payload to CRC */\n"
        f"#define RETROGO_CRC      0x{crc:08X}u\n"
        "\n"
        "#endif /* COMMON_RETROGO_CRC_H */\n"
    )
    print(f"Wrote {OUT.relative_to(REPO)}: LEN=0x{length:08X} ({length} B) CRC=0x{crc:08X}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
