#!/usr/bin/env python3
"""L1 build gate: deterministic asset generation.

The committed src/chainloader/assets_gen.{c,h} are produced by cook_assets.py.
Regenerating them must yield byte-identical output (the regression from the
debugging log, where a cooker that scanned the wrong tree silently changed the
generated bytes). This regenerates them, asserts they are unchanged, and restores
the working tree with `git checkout` regardless of outcome. No device.

  python3 scripts/tests/build/determinism_test.py
"""
from __future__ import annotations

import subprocess
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parents[2]))
from common.harness import TestRun            # noqa: E402

TEST_META = dict(tier="L1", subsystem="build", envs=["ANY"], build=None,
                 observable="host", automated=True, goal=[12])

REPO = Path(__file__).resolve().parents[3]
GEN_C = REPO / "src" / "chainloader" / "assets_gen.c"
GEN_H = REPO / "src" / "chainloader" / "assets_gen.h"
COOK = REPO / "scripts" / "build" / "cook_assets.py"


def main() -> int:
    t = TestRun("deterministic asset generation")
    if not COOK.is_file() or not GEN_C.is_file():
        t.note("cook_assets.py or assets_gen.c absent; skipping")
        return t.summary()

    before = {GEN_C: GEN_C.read_bytes(), GEN_H: GEN_H.read_bytes()}
    try:
        r = subprocess.run(
            ["python3", str(COOK), "--out-c", str(GEN_C), "--out-h", str(GEN_H)],
            cwd=REPO, capture_output=True, text=True, timeout=180)
        if r.returncode != 0:
            t.check(False, f"cook_assets.py failed: {r.stderr.strip()[-300:]}")
            return t.summary()
        after = {p: p.read_bytes() for p in (GEN_C, GEN_H)}
        for p in (GEN_C, GEN_H):
            t.check(before[p] == after[p],
                    f"{p.name} regenerates byte-identical "
                    f"({len(before[p])} B -> {len(after[p])} B)")
    finally:
        # Leave the working tree pristine no matter what.
        subprocess.run(["git", "checkout", "--", str(GEN_C), str(GEN_H)],
                       cwd=REPO, capture_output=True, text=True)
    return t.summary()


if __name__ == "__main__":
    sys.exit(main())
